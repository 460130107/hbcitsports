public class Sports {

	private int id;
	private String sportsname;
	private String sportsbegin;
	private String sportsend;
	private String registend;
	private String address;
	private int current;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the sportsname
	 */
	public String getSportsname() {
		return sportsname;
	}
	/**
	 * @param sportsname the sportsname to set
	 */
	public void setSportsname(String sportsname) {
		this.sportsname = sportsname;
	}
	/**
	 * @return the sportsbegin
	 */
	public String getSportsbegin() {
		return sportsbegin;
	}
	/**
	 * @param sportsbegin the sportsbegin to set
	 */
	public void setSportsbegin(String sportsbegin) {
		this.sportsbegin = sportsbegin;
	}
	/**
	 * @return the sportsend
	 */
	public String getSportsend() {
		return sportsend;
	}
	/**
	 * @param sportsend the sportsend to set
	 */
	public void setSportsend(String sportsend) {
		this.sportsend = sportsend;
	}
	/**
	 * @return the registend
	 */
	public String getRegistend() {
		return registend;
	}
	/**
	 * @param registend the registend to set
	 */
	public void setRegistend(String registend) {
		this.registend = registend;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the current
	 */
	public int getCurrent() {
		return current;
	}
	/**
	 * @param current the current to set
	 */
	public void setCurrent(int current) {
		this.current = current;
	}
	
}
